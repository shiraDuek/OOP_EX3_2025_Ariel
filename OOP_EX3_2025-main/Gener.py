    PSYCHOLOGICAL_DRAMA = "Psychological Drama"
    PHILOSOPHY = "Philosophy"
    EPIC_POETRY = "Epic Poetry"
    GOTHIC_FICTION = "Gothic Fiction"
    GOTHIC_ROMANCE = "Gothic Romance"
    REALISM = "Realism"
    MODERNISM = "Modernism"
    SATIRE = "Satire"
    SCIENCE_FICTION = "Science Fiction"
    FANTASY = "Fantasy"
    TRAGEDY = "Tragedy"
